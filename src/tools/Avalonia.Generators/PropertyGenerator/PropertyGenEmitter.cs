using System.Collections.Generic;
using System.Text;
using Avalonia.Analyzers.GeneratedProperties;

namespace Avalonia.Generators.PropertyGenerator;

/// <summary>
/// Emits the generated partial-type source for one containing type.
/// </summary>
internal static class PropertyGenEmitter
{
    private const string AvaloniaProperty = "global::Avalonia.AvaloniaProperty";
    private const string BindingMode = "global::Avalonia.Data.BindingMode";
    private const string AvaloniaObject = "global::Avalonia.AvaloniaObject";

    public static string Emit(PropertyGenTypeModel model)
    {
        var builder = new StringBuilder();
        builder.AppendLine("// <auto-generated />");
        builder.AppendLine("#nullable enable");
        builder.AppendLine();

        var indent = 0;
        if (model.Type.Namespace is { } ns)
        {
            builder.AppendLine($"namespace {ns}");
            builder.AppendLine("{");
            indent++;
        }

        foreach (var declaration in model.Type.ClassDeclarations)
        {
            Line(builder, indent, declaration);
            Line(builder, indent, "{");
            indent++;
        }

        var firstMember = true;
        foreach (var property in model.Properties)
        {
            EmitProperty(builder, indent, model.Type, property, ref firstMember);
        }

        EmitCallbackDeclarations(builder, indent, model.Properties, ref firstMember);

        while (indent > 0)
        {
            indent--;
            Line(builder, indent, "}");
        }

        return builder.ToString();
    }

    private static void EmitProperty(
        StringBuilder builder,
        int indent,
        TypeDeclarationModel type,
        PropertyGenModel property,
        ref bool firstMember)
    {
        StartMember(builder, indent, ref firstMember);

        switch (property.Kind)
        {
            case GeneratedPropertyKind.Styled:
                EmitFieldDoc(builder, indent, $"Defines the <see cref=\"{property.Name}\"/> property.");
                Line(builder, indent, $"{property.MemberAccessibility} static readonly global::Avalonia.StyledProperty<{property.ValueTypeRef}> {property.Name}Property =");
                Line(builder, indent + 1, RenderStyledRegistration(type, property) + ";");
                EmitChangedRegistration(builder, indent, type, property, ref firstMember);
                StartMember(builder, indent, ref firstMember);
                Line(builder, indent, $"{property.MemberAccessibility} {property.InheritanceModifiers}partial {property.ValueTypeRef} {property.Name}");
                Line(builder, indent, "{");
                Line(builder, indent + 1, $"get => GetValue({property.Name}Property);");
                Line(builder, indent + 1, $"{SetterPrefix(property)}set => SetValue({property.Name}Property, value);");
                Line(builder, indent, "}");
                break;

            case GeneratedPropertyKind.Direct:
                EmitFieldDoc(builder, indent, $"Defines the <see cref=\"{property.Name}\"/> property.");
                Line(builder, indent, $"{property.MemberAccessibility} static readonly global::Avalonia.DirectProperty<{type.OwnerTypeRef}, {property.ValueTypeRef}> {property.Name}Property =");
                Line(builder, indent + 1, RenderDirectRegistration(type, property) + ";");
                EmitChangedRegistration(builder, indent, type, property, ref firstMember);
                StartMember(builder, indent, ref firstMember);
                Line(builder, indent, $"{property.MemberAccessibility} {property.InheritanceModifiers}partial {property.ValueTypeRef} {property.Name}");
                Line(builder, indent, "{");
                Line(builder, indent + 1, "get => field;");
                Line(builder, indent + 1, $"{SetterPrefix(property)}set => SetAndRaise({property.Name}Property, ref field, value);");
                Line(builder, indent, "}");
                break;

            case GeneratedPropertyKind.Attached:
                EmitFieldDoc(builder, indent, $"Defines the <c>{property.Name}</c> attached property.");
                Line(builder, indent, $"{property.MemberAccessibility} static readonly global::Avalonia.AttachedProperty<{property.ValueTypeRef}> {property.Name}Property =");
                Line(builder, indent + 1, RenderAttachedRegistration(type, property) + ";");
                EmitChangedRegistration(builder, indent, type, property, ref firstMember);
                StartMember(builder, indent, ref firstMember);
                Line(builder, indent, $"{property.MemberAccessibility} {property.InheritanceModifiers}static partial {property.ValueTypeRef} Get{property.Name}({property.HostTypeRef} {property.HostParamName})");
                Line(builder, indent + 1, $"=> {property.HostParamName}.GetValue({property.Name}Property);");
                StartMember(builder, indent, ref firstMember);
                Line(builder, indent, "/// <summary>");
                Line(builder, indent, $"/// Sets the value of the <see cref=\"{property.Name}Property\"/> attached property on the specified object.");
                Line(builder, indent, "/// </summary>");
                Line(builder, indent, $"{property.MemberAccessibility} static void Set{property.Name}({property.HostTypeRef} {property.HostParamName}, {property.ValueTypeRef} value)");
                Line(builder, indent + 1, $"=> {property.HostParamName}.SetValue({property.Name}Property, value);");
                break;
        }
    }

    private static string RenderStyledRegistration(TypeDeclarationModel type, PropertyGenModel property)
    {
        if (property.AddOwnerFromTypeRef is { } source)
        {
            var metadata = RenderStyledMetadata(property);
            return $"{source}.{property.Name}Property.AddOwner<{type.OwnerTypeRef}>({metadata})";
        }

        var arguments = new List<string> { $"nameof({property.Name})" };
        if (property.DefaultValueExpr is { } defaultValue)
            arguments.Add($"defaultValue: {defaultValue}");
        if (property.Inherits)
            arguments.Add("inherits: true");
        if (property.DefaultBindingMode is { } mode)
            arguments.Add($"defaultBindingMode: ({BindingMode}){mode}");
        if (property.ValidateMethodName is { } validate)
            arguments.Add($"validate: {validate}");
        if (property.CoerceMethodName is { } coerce)
            arguments.Add($"coerce: {coerce}");
        if (property.EnableDataValidation)
            arguments.Add("enableDataValidation: true");

        return $"{AvaloniaProperty}.Register<{type.OwnerTypeRef}, {property.ValueTypeRef}>({string.Join(", ", arguments)})";
    }

    private static string RenderDirectRegistration(TypeDeclarationModel type, PropertyGenModel property)
    {
        var arguments = new List<string>();
        if (property.AddOwnerFromTypeRef is null)
            arguments.Add($"nameof({property.Name})");
        arguments.Add($"static o => o.{property.Name}");
        if (!property.SetterIsNonPublic)
            arguments.Add($"static (o, v) => o.{property.Name} = v");
        if (property.UnsetValueExpr is { } unsetValue)
            arguments.Add($"unsetValue: {unsetValue}");
        if (property.DefaultBindingMode is { } mode)
            arguments.Add($"defaultBindingMode: ({BindingMode}){mode}");
        if (property.EnableDataValidation)
            arguments.Add("enableDataValidation: true");

        var argumentList = string.Join(", ", arguments);
        return property.AddOwnerFromTypeRef is { } source
            ? $"{source}.{property.Name}Property.AddOwner<{type.OwnerTypeRef}>({argumentList})"
            : $"{AvaloniaProperty}.RegisterDirect<{type.OwnerTypeRef}, {property.ValueTypeRef}>({argumentList})";
    }

    private static string RenderAttachedRegistration(TypeDeclarationModel type, PropertyGenModel property)
    {
        if (property.AddOwnerFromTypeRef is { } source)
        {
            var metadata = RenderStyledMetadata(property);
            return $"{source}.{property.Name}Property.AddOwner<{type.OwnerTypeRef}>({metadata})";
        }

        var arguments = new List<string> { $"\"{property.Name}\"" };
        if (property.OwnerIsStatic)
            arguments.Add($"typeof({type.OwnerTypeRef})");
        if (property.DefaultValueExpr is { } defaultValue)
            arguments.Add($"defaultValue: {defaultValue}");
        if (property.Inherits)
            arguments.Add("inherits: true");
        if (property.DefaultBindingMode is { } mode)
            arguments.Add($"defaultBindingMode: ({BindingMode}){mode}");
        if (property.ValidateMethodName is { } validate)
            arguments.Add($"validate: {validate}");
        if (property.CoerceMethodName is { } coerce)
            arguments.Add($"coerce: {coerce}");

        var typeArguments = property.OwnerIsStatic
            ? $"{property.HostTypeRef}, {property.ValueTypeRef}"
            : $"{type.OwnerTypeRef}, {property.HostTypeRef}, {property.ValueTypeRef}";

        return $"{AvaloniaProperty}.RegisterAttached<{typeArguments}>({string.Join(", ", arguments)})";
    }

    /// <summary>
    /// Metadata overrides for the styled/attached AddOwner path. Only emitted when at least one
    /// override is present; validate cannot be overridden per owner (AVP2002 conflict).
    /// </summary>
    private static string RenderStyledMetadata(PropertyGenModel property)
    {
        var arguments = new List<string>();
        if (property.DefaultValueExpr is { } defaultValue)
            arguments.Add($"defaultValue: {defaultValue}");
        if (property.DefaultBindingMode is { } mode)
            arguments.Add($"defaultBindingMode: ({BindingMode}){mode}");
        if (property.CoerceMethodName is { } coerce)
            arguments.Add($"coerce: {coerce}");
        if (property.EnableDataValidation)
            arguments.Add("enableDataValidation: true");

        return arguments.Count == 0
            ? string.Empty
            : $"new global::Avalonia.StyledPropertyMetadata<{property.ValueTypeRef}>({string.Join(", ", arguments)})";
    }

    private static void EmitChangedRegistration(
        StringBuilder builder,
        int indent,
        TypeDeclarationModel type,
        PropertyGenModel property,
        ref bool firstMember)
    {
        if (property.ChangedMethodName is not { } method)
        {
            return;
        }

        // The subscription is stored in a static field so it runs exactly once per closed type;
        // it must be emitted after the {Name}Property field it reads (static initializer order).
        var target = property.Kind == GeneratedPropertyKind.Attached ? property.HostTypeRef : type.OwnerTypeRef;
        var invocation = property.Kind == GeneratedPropertyKind.Attached
            ? $"{method}(sender, e.OldValue.GetValueOrDefault()!, e.NewValue.GetValueOrDefault()!)"
            : $"sender.{method}(e.OldValue.GetValueOrDefault()!, e.NewValue.GetValueOrDefault()!)";

        StartMember(builder, indent, ref firstMember);
        Line(builder, indent, $"private static readonly global::System.IDisposable _{Camel(property.Name)}ChangedReg =");
        Line(builder, indent + 1, $"global::Avalonia.AvaloniaObjectExtensions.AddClassHandler<{target}, {property.ValueTypeRef}>(");
        Line(builder, indent + 2, $"{property.Name}Property.Changed,");
        Line(builder, indent + 2, $"static (sender, e) => {invocation});");
    }

    private static void EmitCallbackDeclarations(
        StringBuilder builder,
        int indent,
        IReadOnlyList<PropertyGenModel> properties,
        ref bool firstMember)
    {
        // Deduplicate by full declaration text: two properties may share one callback method,
        // while the same name with different parameter types is a valid overload pair.
        var emitted = new HashSet<string>();

        foreach (var property in properties)
        {
            if (property.ChangedMethodName is { } changed)
            {
                var declaration = property.Kind == GeneratedPropertyKind.Attached
                    ? $"private static partial void {changed}({property.HostTypeRef} host, {property.ValueTypeRef} oldValue, {property.ValueTypeRef} newValue);"
                    : $"private partial void {changed}({property.ValueTypeRef} oldValue, {property.ValueTypeRef} newValue);";
                EmitOnce(builder, indent, declaration, emitted, ref firstMember);
            }

            if (property.ValidateMethodName is { } validate)
            {
                EmitOnce(builder, indent,
                    $"private static partial bool {validate}({property.ValueTypeRef} value);",
                    emitted, ref firstMember);
            }

            if (property.CoerceMethodName is { } coerce)
            {
                EmitOnce(builder, indent,
                    $"private static partial {property.ValueTypeRef} {coerce}({AvaloniaObject} sender, {property.ValueTypeRef} value);",
                    emitted, ref firstMember);
            }
        }
    }

    private static void EmitOnce(StringBuilder builder, int indent, string declaration, HashSet<string> emitted, ref bool firstMember)
    {
        if (emitted.Add(declaration))
        {
            StartMember(builder, indent, ref firstMember);
            Line(builder, indent, declaration);
        }
    }

    private static void EmitFieldDoc(StringBuilder builder, int indent, string summary)
    {
        Line(builder, indent, "/// <summary>");
        Line(builder, indent, $"/// {summary}");
        Line(builder, indent, "/// </summary>");
    }

    private static string SetterPrefix(PropertyGenModel property)
        => property.SetterAccessibility is { } accessibility ? accessibility + " " : string.Empty;

    private static void StartMember(StringBuilder builder, int indent, ref bool firstMember)
    {
        if (!firstMember)
        {
            builder.AppendLine();
        }

        firstMember = false;
    }

    private static string Camel(string name)
        => char.ToLowerInvariant(name[0]) + name.Substring(1);

    private static void Line(StringBuilder builder, int indent, string text)
        => builder.Append(' ', indent * 4).AppendLine(text);
}
