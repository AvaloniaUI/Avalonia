namespace Avalonia.Controls
{
    /// <summary>
    /// Defines which edge of the <see cref="DrawerPage"/> the drawer pane slides in from.
    /// </summary>
    public enum DrawerPlacement
    {
        /// <summary>
        /// The drawer slides in from the left edge.
        /// </summary>
        Left,
        /// <summary>
        /// The drawer slides in from the right edge.
        /// </summary>
        Right,
        /// <summary>
        /// The drawer slides down from the top edge.
        /// </summary>
        Top,
        /// <summary>
        /// The drawer slides up from the bottom edge.
        /// </summary>
        Bottom
    }
}
