namespace Avalonia.Input
{
    /// <summary>
    /// Specifies the direction of a swipe gesture.
    /// </summary>
    public enum SwipeDirection
    {
        /// <summary>
        /// The swipe moved to the left.
        /// </summary>
        Left,

        /// <summary>
        /// The swipe moved to the right.
        /// </summary>
        Right,

        /// <summary>
        /// The swipe moved upward.
        /// </summary>
        Up,

        /// <summary>
        /// The swipe moved downward.
        /// </summary>
        Down
    }
}
