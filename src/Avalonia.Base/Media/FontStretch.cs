namespace Avalonia.Media
{
    /// <summary>
    /// FontStretch describes relative change from the normal aspect ratio
    /// as specified by a font designer for the glyphs in a font.
    /// </summary>
    public enum FontStretch
    {
        Normal = 5, 
        UltraCondensed = 1, 
        ExtraCondensed = 2, 
        Condensed = 3, 
        SemiCondensed = 4,
        SemiExpanded = 6, 
        Expanded = 7, 
        ExtraExpanded = 8,
        UltraExpanded = 9
    }
}
