namespace Perspex.Input
{
    /// <summary>
    /// Describes how focus should be moved.
    /// </summary>
    public enum CursorMovementDirection
    {      
        /// <summary>
        /// Moves the cursor to the left.
        /// </summary>
        Left,

        /// <summary>
        /// Moves the cursor to the right.
        /// </summary>
        Right,

        /// <summary>
        /// Moves the cursor up.
        /// </summary>
        Up,

        /// <summary>
        /// Moves the cursor down.
        /// </summary>
        Down,
    }
}